        1. Sobre HTML, podemos afirmar que:
        1. É uma linguagem de Programação, necessária para desenvolvimento de sistemas WEB
        2. É uma linguagem de Programação, necessária para desenvolvimento de sistemas DESKTOP
        3. É uma linguagem de Marcação (ou Estruturação), para definir as divisões de um documento genérico
        4. É uma linguagem de Marcação (ou Estruturação), para definir as seções, com elementos pré-formatados, de páginas web.
        5. Nenhuma das anteriores

    4. Sobre uma TAG, é correto afirmar que
        1. HTML possui um conjunto pré-definido de TAGs, que são representadas pelas notações <TAG> e </TAG>, podendo também ter variações como <TAG/>
        2. HTML possui um conjunto ilimitado de TAGs, nas quais qualquer desenvolvedor pode definir qualquer TAG
        3. HTML possui um conjunto pré-definido de TAGs que são representadas, exclusivamente pelas notações <TAG> e </TAG>
        4. Em HTML é necessário utilizar pelo menos um tipo de cada variação da nomenclatura de TAGs 
        5. Em HTML o uso de TAGs é irrestrito, porém limitado a um número máximo.

    5. Quando falamos que a estrutura de um documento HTML é hierárquica, dizemos que:
        1. Seções (definidas por TAGs) podem - e devem - estar contidas dentro de outras seções
        2. Um documento pode fazer referência a outro documento
        3. A hierarquia refere-se à capacidade de um documento HTML ser editado por diferentes tipos de desenvolvedores
        4. No desenvolvimento de uma página HTML é necessário uma autorização do gestor imediato
        5. Seções são exclusivas e não podem ter outras seções dentro delas.

    6. Sobre a TAG DIV, podemos afirmar que:
        1. É uma TAG que tem um estilo de formatação próprio, com bordas arredondadas e sombra
        2. É uma TAG que permite ao desenvolvedor especificar um novo documento
        3. É uma TAG que não serve para nada
        4. É uma TAG que tem uma característica particular: não pode ter nenhuma outra tag dentro dela.
        5. Ela é uma TAG que define uma divisão (seção) que não tem formatação prévia, servindo para estruturar uma página

    7. [Para pesquisar - de preferência no w3schools.com] A diferença entre elementos do tipo block e inline consiste de:
        1. Ambos são exatamente a mesma coisa, servindo apenas para confundir o designer/programador
        2. Elementos block são elementos que sempre serão exibidos a partir de uma nova linha, enquanto elementos Inline são exibidos dentro da mesma linha
        3. Elementos Block e Inline são elementos que vem com estilos de exibição próprios, assim como Tables, Lists e outros
        4. Elementos Block e Inline não possuem TAGs específicas, permitindo ao desenvolvedor criar suas próprias TAGs
        5. Elementos Inline são elementos de formatação, enquanto que elementos Block são elementos de estruturação.

    8. Sobre TAGs é correto afirmar que
        1. TAGs indicam na verdade instruções de lógica de programação
        2. TAGs podem conter atributos que as modificam
        3. TAGs obrigatoriamente devem ter suas respectivas TAGs de fechamento
        4. TAGs são elementos que servem para que o código do HTML apenas seja legível para o desenvolvedor
        5. TAGs servem apenas para demarcar referências a outros documentos

    9. [Para pesquisar - de preferência no w3schools.com] A Diferença entre uma tag DIV e uma SPAN é:
        1. DIV é uma tag INLINE e SPAN é uma tag BLOCK
        2. DIV e SPAN são tags BLOCK
        3. DIV e SPAN são tags INLINE
        4. DIV é uma tag BLOCK e SPAN é uma tag INLINE
        5. Nenhuma das alternativas

    10. [Para pesquisar - de preferência no w3schools.com] Sobre tratamento multimídia em páginas HTML, podemos dizer que:
        1. só conseguimos colocar elementos para Download exclusivamente
        2. só conseguimos incorporar vídeos do Youtube através do IFRAME
        3. podemos incluir áudio e vídeo através das tags <AUDIO*> e <VIDEO> respectivamente
        4. podemos incluir áudio e vídeo através da mesma tag <MEDIA>
        5. podemos incluir áudio e vídeo desde que utilizemos um plugin externo feito por terceiros

    11. [Para pesquisar - de preferência no w3schools.com] Sobre TABLES, podemos afirmar que:
        1. TH e TD são TAGS que definem novas linhas de conteúdo, podendo ser utilizadas de forma alternativa
        2. TR é uma TAG para a inserção de novas colunas
        3. TH e TD são TAGs que referem-se à itens (colunas) de cabeçalho e dados, respectivamente
        4. TR é uma TAG para definir a legenda de uma tabela
        5. TR, TD e TH são variações para a mesma coisa, ou seja, tabelas.

    12. Sobre a tag A (de Anchor)
        1. seu atributo HREF refere-se exclusivamente a uma seção dentro de uma mesma página
        2. seu atributo HREF pode referir-se tanto a uma nova página, um link externo ou mesmo uma seção dentro da mesma página
        3. seu atributo HREF refere-se apenas a um novo documento
        4. seu atributo HREF refere-se apenas a um site externo
        5. nada do que se diz sobre o HREF é verdadeiro nestas alternativas


